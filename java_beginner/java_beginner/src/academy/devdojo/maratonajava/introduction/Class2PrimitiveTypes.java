package academy.devdojo.maratonajava.introduction;

public class Class2PrimitiveTypes {
    public static void main(String[] args) {

        // Numeric Type Variable
        byte ageByte = 10;
        short ageShort = 10;
        int ageInteger = 10;
        long bigNumber = 2000000000;
        double salaryDouble = 200.0;
        float salaryFloat = 250.0F;
        char charVariable = 87;

        // Type Boolean Variable
        boolean trueBoolean = true;
        boolean falseBoolean = false;

        // String Is A Class
        String name = "Java is so fucking complicated";

        System.out.println("The age is "+ageByte);
        System.out.println(trueBoolean);
        System.out.println(name);
    }
}
